import java.io.FileNotFoundException;
import java.io.FileReader;
import java.io.FileWriter;
import java.io.IOException;
import java.util.Formatter;
import java.util.FormatterClosedException;
import java.util.NoSuchElementException;
import java.util.Scanner;

public class GerenciadorDeArquivos {
    public static void gravaArquivoCsv(ListaObj<Paciente> lista, String nomeArq) {
        FileWriter arq = null;
        Formatter saida = null;
        Boolean deuRuim = false;

        nomeArq += ".csv";

        // Bloco try-catch para abrir o arquivo
        try {
            arq = new FileWriter(nomeArq);
            saida = new Formatter(arq);
        } catch (IOException erro) {
            System.out.println("Erro ao abrir o arquivo");
            System.exit(1);
        }

        // Bloco try-catch para gravar o arquivo
        try {
            for (int i = 0; i < lista.getTamanho(); i++) {
                //Recupere um elemento da lista e formate aqui:
                Paciente paciente = lista.getElemento(i);
                saida.format("%d;%s;%s;%b;%.2f;%d\n", paciente.getId(), paciente.getNome(), paciente.getSintomas(), paciente.getPrioritario(), paciente.getPeso(), paciente.getIdade());
            }
        } catch (FormatterClosedException erro) {
            System.out.println("Erro ao gravar o arquivo");
            deuRuim = true;
        } finally {
            saida.close();
            try {
                arq.close();
            } catch (IOException erro) {
                System.out.println("Erro ao fechar o arquivo");
                deuRuim = true;
            }
            if (deuRuim) {
                System.exit(1);
            }
        }
    }

    public static void leArquivoCsv(String nomeArq) {
        FileReader arq = null;
        Scanner entrada = null;
        Boolean deuRuim = false;

        nomeArq += ".csv";

        // Bloco try-catch para abrir o arquivo
        try {
            arq = new FileReader(nomeArq);
            entrada = new Scanner(arq).useDelimiter(";|\\n");
        } catch (FileNotFoundException erro) {
            System.out.println("Arquivo nao encontrado");
            System.exit(1);
        }

        // Bloco try-catch para ler o arquivo
        try {
            // Print CabeÃ§alho:
            System.out.printf("%4s %-16s %-15s %-11s %5s %6s\n", "id", "nome", "sintomas", "prioritario", "peso", "idade");
            while (entrada.hasNext()) {
                //Print Corpo:
                int id = entrada.nextInt();
                String nome = entrada.next();
                String sintomas = entrada.next();
                boolean prioritario = entrada.nextBoolean();
                Double peso = entrada.nextDouble();
                int idade = entrada.nextInt();

                System.out.printf("%04d %-16s %-15s %-11b %5.1f %6d\n", id, nome, sintomas, prioritario, peso, idade);
            }
        } catch (NoSuchElementException erro) {
            System.out.println("Arquivo com problemas");
            deuRuim = true;
        } catch (IllegalStateException erro) {
            System.out.println("Erro na leitura do arquivo");
            deuRuim = true;
        } finally {
            entrada.close();
            try {
                arq.close();
            } catch (IOException erro) {
                System.out.println("Erro ao fechar o arquivo");
                deuRuim = true;
            }
            if (deuRuim) {
                System.exit(1);
            }
        }
    }
}
